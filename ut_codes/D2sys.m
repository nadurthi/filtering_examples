function f=D2sys(x)
r1=x(1);
w1=x(2);
r2=x(3);
w2=x(4);
r3=x(5);
w3=x(6);
r=0;

f(1)=2*r1^2*w1+4*r2^2*w2+4*r3^2*w3*(1+r^2)-1;
f(2)=2*r1^4*w1+4*r2^4*w2+4*r3^4*w3*(1+r^4)-3;
f(3)=4*r2^4*w2+8*r^2*r3^4*w3-1;
f(4)=2*r1^6*w1+4*r2^6*w2+4*r3^6*w3*(1+r^6)-15;
f(5)=4*r2^6*w2+4*r3^6*w3*(r^2+r^4)-3;
f(6)=2*r1^8*w1+4*r2^8*w2+4*r3^8*w3*(1+r^8)-105;
f=f';
end


